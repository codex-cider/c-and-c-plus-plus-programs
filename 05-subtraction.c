#include <stdio.h>

void main()
{

    int firstNumber, secondNumber, subtraction;

    firstNumber = -20;
    secondNumber = -30;

    subtraction = firstNumber - secondNumber;

    // firstNumber - secondNumber = subtraction
    printf("%d - %d = %d", firstNumber, secondNumber, subtraction);
}