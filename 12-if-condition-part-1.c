#include <stdio.h>

void main()
{
    // if condition
    if (5 > 10)
    {
        printf("5 is greater then 10");
    }
    else
    {
        printf("5 is not greater then 10");
    }

    /*

    >, <, >=, <=, ==, != // conditional operator

all conditional operator will return true OR false

    */
}